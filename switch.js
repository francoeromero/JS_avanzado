//sentencias switch

let nota = 5;

switch(nota){
    case 5:
        console.log("muy bien")
    break;
    case 4:
        console.log(" bien")
    break;
    case 3:
        console.log("normal")
    break;
    case 2:
        console.log("desaprobado")
    break;
    case 1:
        console.log("mal")
    break;
    default:
        console.log("error! escribe bien la nota")
    break;
}